package allprograms;

public class _07_divide248withoutdivope {
	public static void main(String[] args) {
       int n=16;
       //for dividing the num
       System.out.println(n>>1);
       System.out.println(n>>2);
       System.out.println(n>>3);
       //for multiplying the num
       System.out.println(n<<1);
       System.out.println(n<<2);
       System.out.println(n<<3);
	}
}
